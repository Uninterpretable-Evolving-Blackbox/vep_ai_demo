def _run_condition(client, model, system_prompt, query, option_aliases,
                   gt_enabled, gt_disabled, gt_category, vep_options,
                   temperature, seed, label):
    """Run one LLM condition and return (score_dict, response_text)."""
    resp = ""
    try:
        resp = call_llm(client, model, system_prompt, query,
                        temperature=temperature, seed=seed)
        en, dis = extract_recommendations(resp, option_aliases)
        score = score_response(en, dis, gt_enabled, gt_disabled, vep_options, query,
                               gt_category=gt_category)
    except Exception as e:
        # CAVEAT: an infra failure (timeout/OOM) is scored as all-zero here (empty enable/disable sets ->
        # 0 across every metric; resp="" -> use case "unknown", citation 0) and then AVERAGED IN, so it is
        # indistinguishable from "the model got everything wrong" and silently drags the means down. The
        # `_errored` marker below lets a consumer count/exclude these failed calls (none currently does).
        print(f"ERROR: {e}")
        score = score_response(set(), set(), gt_enabled, gt_disabled, vep_options, query,
                               gt_category=gt_category)
        score["_errored"] = True

    # Interpretability metrics
    detected_uc = extract_use_case(resp)
    score["use_case_detected"] = detected_uc
    score["use_case_correct"] = (detected_uc == gt_category)
    cit_rate, cit_found, cit_total = measure_citation_rate(resp)
    score["citation_rate"] = cit_rate
    score["citations_found"] = cit_found
    score["total_recommendations"] = cit_total
    score["_response"] = resp        # raw text, for re-scoring after parser/metric changes
    return score


def main():
    parser = argparse.ArgumentParser(description="Evaluate VEP AI Assistant")
    parser.add_argument("--model", default=None,
                        help="Ollama model name (default: VEP_MODEL env var or qwen2.5:3b)")
    parser.add_argument("--semantic", action="store_true",
                        help="Include semantic retrieval condition in evaluation")
    parser.add_argument("--all-examples", action="store_true",
                        help="Include 'all examples' condition (no retrieval, all training examples)")
    parser.add_argument("--runs", type=int, default=1,
                        help="Number of evaluation runs per configuration (default: 1)")
    parser.add_argument("--seed", type=int, default=42,
                        help="Base random seed for reproducibility (default: 42)")
    parser.add_argument("--temperature", type=float, default=0.7,
                        help="Sampling temperature (default: 0.7; use 0.0 for deterministic/greedy)")
    args = parser.parse_args()

    base_url = os.environ.get("OLLAMA_BASE_URL", "http://localhost:11434/v1")
    model = args.model or os.environ.get("VEP_MODEL", "qwen2.5:3b")
    use_semantic = args.semantic
    use_all_examples = args.all_examples
    num_runs = args.runs
    base_seed = args.seed
    temperature = args.temperature

    num_conditions = 2 + int(use_all_examples) + int(use_semantic)
    total_calls = len(TEST_QUERIES) * num_conditions * num_runs

    cond_names = ["bare", "keyword"]
    if use_all_examples:
        cond_names.append("all-examples")
    if use_semantic:
        cond_names.append("semantic")

    print(f"Model: {model}")
    print(f"Ollama: {base_url}")
    print(f"Test queries: {len(TEST_QUERIES)}")
    print(f"Conditions: {' + '.join(cond_names)}")
    print(f"Runs: {num_runs} (seeds: {base_seed}\u2013{base_seed + num_runs - 1})")
    print(f"Temperature: {temperature}")
    print(f"Total LLM calls: {total_calls}")
    print("Evaluation mode: leave-one-out (ground truth excluded from retrieval)")
    print()

    vep_options, training_examples = load_knowledge_base()
    option_aliases = build_option_aliases(vep_options)
    client = OpenAI(base_url=base_url, api_key="ollama")

    # Pre-build system prompts (they don't change between runs)
    sys_prompt_kw_cache = {}
    sys_prompt_all_cache = {}
    sys_prompt_sem_cache = {}

    all_results = {
        "model": model,
        "num_runs": num_runs,
        "base_seed": base_seed,
        "temperature": temperature,
        "queries": [],
    }

    for i, test in enumerate(TEST_QUERIES, 1):
        query = test["query"]
        gt_id = test["ground_truth_id"]
        gt_category, gt_enabled, gt_disabled = get_ground_truth(
            training_examples, gt_id
        )

        print(f"[{i}/{len(TEST_QUERIES)}] {test['id']}")
        print(f"  Ground truth: {gt_category} ({len(gt_enabled)} en / {len(gt_disabled)} dis)")

        # Leave-one-out: exclude the ground truth example from the retrieval
        # corpus so the model is never shown the exact answer it's scored against.
        loo_examples = [ex for ex in training_examples if ex["id"] != gt_id]

        # Cache system prompts per query (each query's LOO set is unique)
        if query not in sys_prompt_kw_cache:
            sys_prompt_kw_cache[query] = build_system_prompt(
                vep_options, loo_examples, query, retrieval_mode="keyword"
            )
        sys_prompt_kw = sys_prompt_kw_cache[query]

        if use_all_examples and query not in sys_prompt_all_cache:
            sys_prompt_all_cache[query] = build_system_prompt(
                vep_options, loo_examples, query, retrieval_mode="all"
            )
        sys_prompt_all = sys_prompt_all_cache.get(query)

        if use_semantic and query not in sys_prompt_sem_cache:
            sys_prompt_sem_cache[query] = build_system_prompt(
                vep_options, loo_examples, query, retrieval_mode="semantic"
            )
        sys_prompt_sem = sys_prompt_sem_cache.get(query)

        run_scores_w = []
        run_scores_wo = []
        run_scores_all = []
        run_scores_s = []

        for run_idx in range(num_runs):
            current_seed = base_seed + run_idx
            run_label = f"  Run {run_idx + 1}/{num_runs} (seed={current_seed})" if num_runs > 1 else ""
            if run_label:
                print(run_label)

            # --- With KB (keyword) ---
            print("    WITH KB (keyword)...", end=" ", flush=True) if num_runs > 1 else print("  Running WITH KB (keyword)...", end=" ", flush=True)
            score_w = _run_condition(
                client, model, sys_prompt_kw, query, option_aliases,
                gt_enabled, gt_disabled, gt_category, vep_options,
                temperature, current_seed, "keyword",
            )
            print(f"done (F1={_pct(score_w['enable_f1'])})")   # _pct: enable_f1 is None on empty/errored output
            run_scores_w.append(score_w)

            # --- Without KB ---
            print("    WITHOUT KB...", end=" ", flush=True) if num_runs > 1 else print("  Running WITHOUT KB...", end=" ", flush=True)
            score_wo = _run_condition(
                client, model, BARE_SYSTEM_PROMPT, query, option_aliases,
                gt_enabled, gt_disabled, gt_category, vep_options,
                temperature, current_seed, "bare",
            )
            print(f"done (F1={_pct(score_wo['enable_f1'])})")
            run_scores_wo.append(score_wo)

            # --- With KB (all examples) ---
            if use_all_examples:
                print("    WITH KB (all examples)...", end=" ", flush=True) if num_runs > 1 else print("  Running WITH KB (all examples)...", end=" ", flush=True)
                score_a = _run_condition(
                    client, model, sys_prompt_all, query, option_aliases,
                    gt_enabled, gt_disabled, gt_category, vep_options,
                    temperature, current_seed, "all",
                )
                print(f"done (F1={_pct(score_a['enable_f1'])})")
                run_scores_all.append(score_a)

            # --- With KB (semantic) ---
            if use_semantic:
                print("    WITH KB (semantic)...", end=" ", flush=True) if num_runs > 1 else print("  Running WITH KB (semantic)...", end=" ", flush=True)
                score_s = _run_condition(
                    client, model, sys_prompt_sem, query, option_aliases,
                    gt_enabled, gt_disabled, gt_category, vep_options,
                    temperature, current_seed, "semantic",
                )
                print(f"done (F1={_pct(score_s['enable_f1'])})")
                run_scores_s.append(score_s)

        # Aggregate across runs
        agg_w = aggregate_scores(run_scores_w)
        agg_wo = aggregate_scores(run_scores_wo)

        result_entry = {
            "id": test["id"],
            "query": query,
            "source": test.get("source", "synthetic"),
            "gt_category": gt_category,
            "with_kb": agg_w,
            "without_kb": agg_wo,
        }
        if run_scores_all:
            result_entry["with_kb_all"] = aggregate_scores(run_scores_all)
        if run_scores_s:
            result_entry["with_kb_semantic"] = aggregate_scores(run_scores_s)
        all_results["queries"].append(result_entry)
        print()

    # Generate and save report
    report = generate_report(all_results)

    results_dir = Path(os.environ.get("VEP_RESULTS_DIR", BASE_DIR / "results"))
    results_dir.mkdir(parents=True, exist_ok=True)
    safe_model = model.replace("/", "_").replace(":", "_")
    out_path = results_dir / f"evaluation_results_{safe_model}.md"
    with open(out_path, "w") as f:
        f.write(report)
    print(f"Report saved to: {out_path}")

    # Print summary to terminal.
    # NOTE: this terminal line headlines the UNWEIGHTED enable_f1 (emphasis only — the saved .md report
    # shows both unweighted and priority-weighted). The project's stated headline is the weighted metric, so
    # prefer the .md report / enable_recall_weighted. (Also: the r.get("with_kb_all", r["without_kb"]) calls
    # below are the same silent bare-substitution as _get — a missing 'all'/'semantic' condition reads as bare.)
    print("\n" + "=" * 60)
    avg_f1_w = _safe_mean([r["with_kb"]["enable_f1"] for r in all_results["queries"]]) or 0   # None-safe terminal line
    avg_f1_wo = _safe_mean([r["without_kb"]["enable_f1"] for r in all_results["queries"]]) or 0
    summary = f"  Enable F1 — With KB (kw): {avg_f1_w:.0%}  |  Without KB: {avg_f1_wo:.0%}  |  \u0394: {avg_f1_w - avg_f1_wo:+.0%}"
    if use_all_examples:
        avg_f1_a = _safe_mean([r.get("with_kb_all", r["without_kb"])["enable_f1"] for r in all_results["queries"]]) or 0
        summary += f"\n  Enable F1 — With KB (all): {avg_f1_a:.0%}  |  \u0394 vs bare: {avg_f1_a - avg_f1_wo:+.0%}  |  \u0394 vs kw: {avg_f1_a - avg_f1_w:+.0%}"
    if use_semantic:
        avg_f1_s = _safe_mean([r.get("with_kb_semantic", r["without_kb"])["enable_f1"] for r in all_results["queries"]])
        summary += f"\n  Enable F1 — With KB (sem): {_pct(avg_f1_s)}  |  Δ vs bare: {_pct_delta(avg_f1_s, avg_f1_wo)}  |  Δ vs kw: {_pct_delta(avg_f1_s, avg_f1_w)}"
    print(summary)
    print("=" * 60)


if __name__ == "__main__":
    main()
