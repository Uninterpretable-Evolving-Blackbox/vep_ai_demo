def extract_use_case(response_text):
    """Extract the detected use case category from LLM response text."""
    response_lower = response_text.lower().replace("-", "_").replace(" ", "_")
    for cat in USE_CASE_CATEGORIES:
        if cat in response_lower:
            return cat
    # Fallback: check without underscores
    for cat in USE_CASE_CATEGORIES:
        parts = cat.split("_")
        if len(parts) >= 2 and all(p in response_lower for p in parts):
            return cat
    return "unknown"


def measure_citation_rate(response_text):
    """DEPRECATED (2026-06-13) — superseded by attribution (run_attribution.py); do not report.

    Measures FORMAT COMPLIANCE (does a line contain '[source:'), NOT citation validity, and is
    unreliable: (1) it miscounts the indented 'Reason:' line as an uncited recommendation (the [source:]
    tag is on the ✓ line above) -> ~-14% deflation on logged data (honest=100%, reported=86%); (2) the
    `option_names` list below is a HARDCODED demo-era (26-name) vocabulary, NOT derived from the KB despite
    the prior docstring's claim -> 32/58 expanded-catalogue ids are unrecognised and ~25% of real ✓/✗
    lines are invisible; (3) substring matching has no word boundaries ("protein"⊂"protein_coding");
    (4) it cannot tell a faithful citation from a hallucinated one ('[source: nonsense]' counts as cited),
    and since the prompt forces a [source:] on every line a correct version is ~always 100% (uninformative).
    The meaningful signal — is the citation correct/grounded — is faithfulness, measured causally by
    run_attribution.py. Kept only so legacy report code runs. See EXPERIMENTS.md 'Metric deprecation'.
    """
    recommendation_lines = 0
    cited_lines = 0
    # All option IDs from the knowledge base, plus common short names that
    # an LLM might use instead of the canonical ID.
    option_names = [
        "sift", "polyphen", "cadd", "clinvar", "gnomad", "hgvs",
        "symbol", "mane", "canonical", "pick", "regulatory",
        "spliceai", "revel", "alphamissense", "transcript",
        "domains", "protein", "check_existing", "gene_phenotype",
        "af_1kg", "most_severe", "per_gene", "maxentscan",
        "gnomad_sv", "clinvar_sv", "summary",
    ]
    for line in response_text.split("\n"):
        line_lower = line.lower().strip()
        # A recommendation line has a checkmark or enable/disable language
        is_rec = any(marker in line for marker in ["\u2713", "\u2717"]) or \
                 re.search(r"(enabl|disabl)", line_lower)
        if not is_rec:
            continue
        if any(name in line_lower for name in option_names):
            recommendation_lines += 1
            if re.search(r"\[source\s*:", line_lower):
                cited_lines += 1
    rate = cited_lines / recommendation_lines if recommendation_lines > 0 else 0.0
    return rate, cited_lines, recommendation_lines


